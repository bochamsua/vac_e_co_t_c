/**
 * Worksheet helper
 *
 * @category    BS
 * @package     BS_Worksheet
 * @author Bui Phong
 */
class BS_Worksheet_Helper_Worksheet extends Mage_Core_Helper_Abstract
{

    /**
     * get base files dir
     *
     * @access public
     * @return string
     * @author Bui Phong
     */
    public function getFileBaseDir()
    {
        return Mage::getBaseDir('media').DS.'worksheet'.DS.'file'.DS;
    }

    /**
     * get base file url
     *
     * @access public
     * @return string
     * @author Bui Phong
     */
    public function getFileBaseUrl()
    {
        return Mage::getBaseUrl('media').'worksheet'.'/'.'file/';
    }
}
